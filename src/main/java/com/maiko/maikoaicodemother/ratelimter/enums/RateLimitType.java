package com.maiko.maikoaicodemother.ratelimter.enums;

/**
 * 限流策略枚举类
 * <p>
 * 定义了系统中支持的三种限流维度，用于在网关层或业务层对请求进行流量控制。
 * 不同的策略适用于不同的安全与稳定性场景。
 * </p>
 *
 * @author Maiko7
 */
public enum RateLimitType {

    /**
     * 接口级别限流
     * <p>
     * 针对特定的 API 端点（URL）进行总请求数限制。
     * 适用于保护后端服务不被整体过载，无论请求来自哪个用户或 IP。
     * </p>
     */
    API,

    /**
     * 用户级别限流
     * <p>
     * 针对特定的登录用户（通常基于 UserID 或 API Key）进行限制。
     * 适用于防止单个恶意用户滥用系统资源，保障其他正常用户的体验。
     * </p>
     */
    USER,

    /**
     * IP级别限流
     * <p>
     * 针对客户端 IP 地址进行限制。
     * 适用于防御未知的网络攻击、爬虫抓取或未登录状态下的恶意刷接口行为。
     * </p>
     */
    IP
}